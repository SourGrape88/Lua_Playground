    player = spawn({
        x = 400,
        y = 300,
        width = 64,
        height = 64,
        vx = 0,
        vy = 0,
        speed = 8,

        update = function(self)
            local new_x = self.x
            local new_y = self.y

            if btn("a") then self.vx = self.vx - 0.5 end
            if btn("d") then self.vx = self.vx + 0.5 end
            if btn("w") then self.vy = self.vy - 0.5 end
            if btn("s") then self.vy = self.vy + 0.5 end

            local size = 64

            if not map:check_collision(new_x, self.y, size, size) then
                self.x = new_x
            end
            if not map:check_collision(self.x, new_y, size, size) then
                self.y = new_y
            end

            Physics.apply_gravity(self)
            Physics.apply_friction(self, 0.1)
            Physics.move(self, map)
        end,

        draw = function(self)
            rect(math.floor(self.x), math.floor(self.y), self.width, self.height, {0, 200, 60})
        end
    })

    local map_width = 26 * 32
    local map_height = 20 * 32

    camera:clamp(
        0, 0,
        map_width - 1000,
        map_height - 800
    )

    camera:follow(player)
end

function _update()
    update_entities()
    camera:update()
end

function _draw()
    map:draw()
    draw_entities()
end
